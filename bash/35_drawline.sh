
case "$(basename $SHELL)" in
  (bash) LINEBREAK_ECHO="echo -ne" ;;
  (zsh) LINEBREAK_ECHO="echo -e" ;;
esac

function drawlinebreak {
  if [ "$TERM" != eterm-color ]; then
    $LINEBREAK_ECHO "\033[1;30m$(substr "$LINEBREAK" 0 "$COLUMNS")\033[0m"
  fi
  LINEBREAK="———————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————"
}
