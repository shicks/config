function drawlinebreak {
  if [ "$TERM" != eterm-color ]; then
    echo -ne "\033[1;30m${LINEBREAK:0:$COLUMNS}\033[0m"
  fi
  LINEBREAK="———————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————"
}
